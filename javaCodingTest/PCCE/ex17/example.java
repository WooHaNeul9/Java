package PCCE.ex17;

public class example {
    public static void main(String[] args) {

        int a = 123;
        // ! String.valueOf(변환할 변수) ---- 123
        String aa = String.valueOf(a);

        System.out.println("숫자 :" + aa);

        // ======================================================//

        int b = 123;
        // ! Integer.toString(변환할 변수) ---- 123
        String bb = Integer.toString(b);

        System.out.println("숫자 :" + bb);

    }

}
